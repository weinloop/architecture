EX = ROOT / "_generated_exam"


def exam(name: str) -> str:
    return (EX / f"{name}.exam.md").read_text(encoding="utf-8")


def write(name: str, header: str) -> None:
    stem = name.replace(".md", "")
    frag = EX / f"{stem}.exam.md"
    body = frag.read_text(encoding="utf-8") if frag.exists() else ""
    if body:
        text = header.rstrip() + "\n\n" + body
    else:
        text = header.rstrip()
    (ROOT / name).write_text(text + "\n", encoding="utf-8")
    print("wrote", name)


